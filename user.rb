class User
  def initialize(sex, height, weight, age, judge,active)
    @sex = sex
    @height = height
    @weight = weight
    @age = age
    @judge = judge
    @active = active
  end

  def sex
    @sex
  end

  def height
    @height
  end

  def weight
    @weight
  end

  def age
    @age
  end

  def judge
    @judge
  end

  def active
    @active
  end
end
